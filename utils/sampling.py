def mnist_iid(dataset, num_users):
    """
    Sample I.I.D. client data from MNIST dataset
    :param dataset:
    :param num_users:
    :return: dict of image index
    """
    dict_users = {}
    num_items = int(len(dataset) / num_users )
    all_idxs = [i for i in range(len(dataset))]
    for i in range(num_users):
        dict_users[i] = set(np.random.choice(all_idxs, num_items, replace=False))
        all_idxs = list(set(all_idxs) - dict_users[i])
    return dict_users

def mnist_iid_notuniform(dataset, num_users):
    #创建一种分配数量不同但依然满足独立同分布的数据分配方法
    min_samples = int(len(dataset) / num_users) - 100
    max_samples = int(len(dataset) / num_users) + 100
    num_items = np.random.randint(min_samples, max_samples, size=num_users)
    total_samples = sum(num_items)
    all_idxs = [i for i in range(len(dataset))]

    # Shuffle the indices to ensure randomness
    np.random.shuffle(all_idxs)

    # Ensure that the total_samples matches the original dataset size
    if total_samples > len(dataset):
        raise ValueError("Total samples requested exceeds the size of the dataset.")

    dict_users = {}
    start_idx = 0
    for i in range(num_users):
        end_idx = start_idx + num_items[i]
        dict_users[i] = set(all_idxs[start_idx:end_idx])
        start_idx = end_idx

    # Ensure all indices are assigned
    assert start_idx == total_samples, "Not all indices have been assigned."

    return dict_users

def mnist_noniid(dataset, num_users):
    """
    Sample non-I.I.D client data from MNIST dataset
    :param dataset:
    :param num_users:
    :return:
    """
    dict_users = {}
    num_shards, num_imgs = num_users * 2, int(len(dataset) / (num_users * 2))
    idx_shard = [i for i in range(num_shards)]
    dict_users = {i: np.array([], dtype='int64') for i in range(num_users)}
    idxs = np.arange(num_shards * num_imgs)
    labels = dataset.train_labels.numpy()

    # sort labels
    idxs_labels = np.vstack((idxs, labels))
    idxs_labels = idxs_labels[:, idxs_labels[1, :].argsort()]
    idxs = idxs_labels[0, :]

    # divide and assign
    for i in range(num_users):
        rand_set = set(np.random.choice(idx_shard, 2, replace=False))
        idx_shard = list(set(idx_shard) - rand_set)
        for rand in rand_set:
            dict_users[i] = np.concatenate((dict_users[i], idxs[rand * num_imgs:(rand + 1) * num_imgs]), axis=0)
    return dict_users

